// function to show more data in patrons
function myFunction() {
  var dots = document.getElementById("director_read_more");
  var moreText = document.getElementById("more_director_info");
  var btnText = document.getElementById("clicked_on_director_info");


  if (dots.style.display === "none") {
    dots.style.display = "inline";
    btnText.innerHTML = "Read more";
    moreText.style.display = "none";
  } else {
    dots.style.display = "none";
    btnText.innerHTML = "Read less";
    moreText.style.display = "inline";
  }

}
